export type CategoryIconType = keyof typeof CAT_ICONS;

interface Props {
  icon: CategoryIconType; // Icon's name alternate prop
  size?: 'small' | 'medium' | 'large' | 'xl' | 'full'; // Icon's name alternate prop,
  sx?: ObjectPropByName;
}

/**
 * Renders SVG icon by given Icon name
 * @component AppIcon
 * @param {string} [props.name] - name of the Icon to render
 * @param {string} [props.icon] - name of the Icon to render
 */
const CategoryIcon: FunctionComponent<Props> = ({ icon, size = 'md', sx, ...restOfProps }) => {
  const ComponentToRender = CAT_ICONS[icon];
  const currentSize =
    size === 'small' ? '16px' : size === 'large' ? '32px' : size === 'xl' ? '40px' : size === 'full' ? '100%' : '24px'; // no size === md
  return (
    <Stack alignItems="center" justifyContent="center" sx={{ fontSize: currentSize, ...sx }}>
      <ComponentToRender {...restOfProps} />
    </Stack>
  );
};

export default CategoryIcon;
